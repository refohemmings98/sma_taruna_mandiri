<b><h2> Tentang Sekolah Taruna Mandiri </h2></b>

<br><h3> Sejarah Sekolah Taruna Mandiri </h3></br>

<br>Sekolah Taruna Mandiri dibangun di tempat yang sangat strategis, yaitu di Jalan Raya Pamulang II No. 3 Pamulang, Kota Tangerang Selatan, dengan luas area 8.524 meter persegi. Sekolah ini diresmikan pada hari Kamis, 30 Juni 2005 dengan memberikan pelayanan pendidikan dari tingkat SD (Sekolah Dasar), Sekolah Menengah Pertama (SMP), sampai Sekolah Menegah Atas (SMA).</br>

<br>Tujuan dari Sekolah Taruna Mandiri adalah memberikan pelayanan pendidikan berkualitas kepada masyarakat dengan harapan menghasilkan peserta didik yang mampu menyelesaikan masalah secara kreatif, memiliki kemampuan untuk memimpin dengan etos kerja yang tinggi, mandiri, berakhlak mulia dan bangga akan budaya bangsanya.</br>

<br><h3>Visi dan Misi</h3></br>

<h4>Visi</h4>
<ol>Membangun generasi muda mandiri, yang beretos kerja tinggi dan berjiwa pemimpin.
</ol>

<h4>Misi</h4>
<ol>
	<li>Menyiapkan pemimpin mada depan yang menguasai IPTEK dan memiliki daya saing tinggi.</li>
	<li>Mewujudkan pribadi-pribadi yang berahlak mulia yang bangga terhadap budaya bangsa.</li>
	<li>Meningkatkan kemampuan intelektual keterampilanpraktis dan kemampuan dalam memecahkan masalah.</li>
</ol>

<br>Dengan penawaran strategi ini, ke depan anak akan mampu mengaplikasikan proses berfikir kritis, kreatif, mandiri, rasa tanggungjawab, dan percaya diri tinggi serta interaksi sosial dalam kehidupan nyata dengan berbekal keseimbangan antara iptek (Ilmu Pengetahuan dan Teknologi) dan Imtaq (iman dan Taqwa). Dengan demikian peserta didik dapat mendemonstrasikan apa yang mereka mengerti dan ketahui dalam cara berbeda dengan baik dan benar.<br>
